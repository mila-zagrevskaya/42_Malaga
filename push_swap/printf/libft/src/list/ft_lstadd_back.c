#include "../../include/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *newl)
{
	t_list	*tmp;

	if (!lst || !newl)
		return ;
	if (!(*lst))
	{
		*lst = newl;
		return ;
	}
	tmp = *lst;
	while (tmp->next)
		tmp = tmp->next;
	tmp->next = newl;
}
